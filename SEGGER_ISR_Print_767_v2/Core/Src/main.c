/* USER CODE BEGIN 4 */
uint8_t MenuButton_Debounce(void) //Menu Button debounce function
{
	uint8_t 		ret = DISABLE;
	static uint16_t Level = Restart_Level;
	if(!HAL_GPIO_ReadPin(menu_button_GPIO_Port, menu_button_Pin))
	{
		++Level;
	}
	else
	{
		--Level;
		if(Level <= Restart_Level)
		{
			Level = Restart_Level;
		}
	}
	if(Level >= 4000)
	{
		Level = Acceptance_Level;
		ret = ENABLE;
	}
	return ret;
}

