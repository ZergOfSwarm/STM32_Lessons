{
  /* USER CODE BEGIN Start_PID_Task */
	uint16_t raw;
  /* Infinite loop */
  for(;;)
  {
		// Get ADC value
		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
		raw = HAL_ADC_GetValue(&hadc1);

		value = (raw - in_min) * (out_max - out_min) / (in_max - in_min)
				+ out_min; // Function map

		if (value >= 8840) {
			value = 9001; // До этого значения никогда не дойдет
			//printf("Value is: %d\r\n",value);
		} else if (value <= 15) {
			value = 0;
			//printf("Value is: %d\r\n",value);
		} else {
			value = value;
			//printf("Value is: %d\r\n",value);
		}

		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, value);

		//osDelay(1);
  }

  /* USER CODE END Start_PID_Task */
}

